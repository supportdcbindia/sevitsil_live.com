      <h2>Description</h2>
      <p class="mb-2">Product build up or blinding on vibrating screens is an ongoing challenge in many production facilities, decreasing production and increasing downtime. When deblinding balls are excited in a vibrating separator environment, they release near-size particles that would otherwise clog screens.</p>
      <p class="mb-2">In a round separator machine the balls are typically supported by a coarser screen located below the sizing screen. Vertical separator motion bounces the balls between the two screens. You want the proper combination of ball diameter and vertical motion to bounce the balls off the underside of the sizing screen with sufficient force to dislodge near-size particles.</p>
      <p>Balls excel at deblinding near-size particles that are jagged or otherwise irregularly shaped. In fact, they typically outperform sliders in such applications. Producers also consider screening balls for use with shear-sensitive products. By themselves, balls tend to migrate to the outer edge of a round screen. As a result, cleaning is sometimes better at the periphery of the screen than at the centre.</p>
